// Events

$('.header_menu .button > a, .footer_menu .button > a').click(toggleSubMenu);
$('#game').click(hideAllSubMenus);

function toggleSubMenu (e) {
	e.preventDefault();
	$(this).siblings('.sub_menu').slideToggle();
}

function hideAllSubMenus(e) {
	e.preventDefault();
	$('.sub_menu').slideUp();
}