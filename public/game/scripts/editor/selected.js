selected.prototype.new = function (id, name, position, scale) {
	var url = '/editor/selected/?id=' + id;
	url += '&name=' + name;
	url += '&positionX=' + Math.round(position.x);
	url += '&positionY=' + Math.round(position.y);
	url += '&positionZ=' + Math.round(position.z);
	url += '&scale=' + scale;
	var callback = function(result) {
		$('.footer_menu li.selected small').html('1');
		$('.footer_menu li.selected ul').html(result);
	}
	$.ajax({
		url: url,
		success: callback
	});
}
